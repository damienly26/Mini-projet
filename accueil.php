<h2>Le CSS</h2>
<div class="texte">
  <p>Le CSS est un langage informatique qui décrit l'apparence des documents HTML et XML. Ce langage est composé de ce qu’on appelle « feuilles de style en cascade » (ou fichiers CSS / .css) et contient des éléments de codage, par exemple, les marges (margin), la couleur de fond (background-color) ou la police de caractère (font-type et font-size).
  La version actuelle de CSS utilisée dans le monde entier est le CSS3. Les nouveautés offertes par CSS3 sont les suivantes :</p>
  <ul>
    <li>Animations</li>
    <li>Arrière-plans multiples et dégradés</li>
    <li>Médias queries : module permettant d'adapter le contenu d'une page web aux caractéristiques de l'appareil de l'utilisateur.</li>
    <li>Nouveaux sélecteurs : notamment les pseudo-classes (mot-clé qui peut être ajouté à un sélecteur afin d'indiquer l'état spécifique dans lequel l'élément doit être pour être ciblé par la déclaration) et pseudo-elements (mot-clé ajouté à un sélecteur qui permet de mettre en forme certaines parties de l'élément ciblé par la règle).</li>
    <li>Coins arrondis (border-radius)</li>
    <li>Gestion de la transparence</li>
    <li>Effets d'ombres</li>
  </ul>
  <p>Et bien d’autres !</p>
</div>

